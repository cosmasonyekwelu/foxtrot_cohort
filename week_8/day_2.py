# A model is a python class that represent a table in a database
# A database is an organized collection of data, typically electronic that is stored and managed for efficient retrieval
# Relational (Tabular format) and non-relational database (Document format)

# A non-relational database stores data in flexible formats such as documents, key-value, graphs, or wide-column stores.
# There is no fixed schema, so you can store anything inside — JSON, arrays, nested objects.
# Think of it like storing flexible JSON files.

# | Non-Relational DB      | Type            | Description                           |
# | ---------------------- | --------------- | ------------------------------------- |
# | **MongoDB**            | Document store  | Stores JSON-like documents            |
# | **Redis**              | Key-value store | Extremely fast in-memory DB           |
# | **Cassandra**          | Wide-column     | Used for high-volume distributed data |
# | **Firebase Firestore** | Document store  | Used in mobile/web apps               |
# | **Neo4j**              | Graph DB        | Stores relationships & graph data     |

# A relational database stores data in tables (rows & columns).
# These tables have relationships between them, using primary keys and foreign keys.

# Think of it like Excel spreadsheets where tables link to each other.
# | Relational DB            | Description                          |
# | ------------------------ | ------------------------------------ |
# | **MySQL**                | Very popular open-source SQL DB      |
# | **PostgreSQL**           | Advanced SQL DB with strong features |
# | **Oracle DB**            | Enterprise-grade SQL database        |
# | **Microsoft SQL Server** | Microsoft’s relational database      |
# | **SQLite**               | Lightweight file-based SQL DB        |
